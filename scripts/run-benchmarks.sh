#!/usr/bin/env bash
#
# run-benchmarks.sh - sweep the strategies across thread counts, loop counts,
# and sharing modes, and record real wall-clock timings.
#
# Output:
#   benchmark-results/raw-results.csv   one row per (strategy, config, repeat)
#   benchmark-results/summary.md        mean timing table + environment notes
#
# Notes:
#   * The -d (deadlock ordering) flag is never used here: benchmarks must
#     terminate. The naive "deadlock" strategy is safe without -d, so it is
#     included as the zero-overhead baseline.
#   * "nolock" in shared mode is included on purpose even though it is
#     incorrect there - the whole point is to show speed without correctness.
#     Its numbers are marked accordingly in the summary.
#   * No result is fabricated: every number comes from a real run on this host.

set -u
cd "$(dirname "$0")/.."

OUTDIR=benchmark-results
CSV=$OUTDIR/raw-results.csv
SUMMARY=$OUTDIR/summary.md
mkdir -p "$OUTDIR"

STRATEGIES=(deadlock global-order try-wait avoid-hold-and-wait nolock)
THREADS=(1 2 4 8)
LOOPS=(1000 10000 100000)
REPEATS=3

echo "==> Building (make all)"
make all >/dev/null || { echo "build failed"; exit 1; }

# Extract the seconds from a "... time=<sec> s" line.
extract_time() { sed -n 's/.*time=\([0-9.][0-9.]*\) s.*/\1/p'; }

echo "strategy,threads,loops,mode,repeat,seconds" > "$CSV"

total=0
for s in "${STRATEGIES[@]}"; do
    for mode in shared parallel; do
        pflag=""; [ "$mode" = parallel ] && pflag="-p"
        for n in "${THREADS[@]}"; do
            for l in "${LOOPS[@]}"; do
                for r in $(seq 1 "$REPEATS"); do
                    line=$("./bin/vector-$s" -t $pflag -n "$n" -l "$l" 2>/dev/null \
                           | extract_time)
                    [ -z "$line" ] && line="NA"
                    echo "$s,$n,$l,$mode,$r,$line" >> "$CSV"
                    total=$((total + 1))
                done
            done
        done
    done
    echo "  done: $s"
done
echo "==> $total measurements written to $CSV"

# --- Build the Markdown summary (mean seconds per configuration) -----------
{
    echo "# Benchmark Results"
    echo
    echo "Generated by \`scripts/run-benchmarks.sh\`. Every number is the mean of"
    echo "$REPEATS runs measured on this host - nothing here is hand-written."
    echo
    echo "## Environment"
    echo
    echo '```'
    echo "date:     $(date -u '+%Y-%m-%dT%H:%M:%SZ')"
    echo "uname:    $(uname -srm)"
    if [ "$(uname)" = Darwin ]; then
        echo "cpu:      $(sysctl -n machdep.cpu.brand_string 2>/dev/null)"
        echo "cores:    $(sysctl -n hw.ncpu 2>/dev/null)"
    else
        echo "cpu:      $(grep -m1 'model name' /proc/cpuinfo 2>/dev/null | cut -d: -f2 | sed 's/^ //')"
        echo "cores:    $(nproc 2>/dev/null)"
    fi
    echo "compiler: $(${CC:-cc} --version | head -1)"
    echo '```'
    echo
    echo "## Mean wall-clock time (seconds)"
    echo
    echo "Rows are strategies; columns are (mode, threads) at loops=${LOOPS[${#LOOPS[@]}-1]}."
    echo
    # Table at the largest loop count, which best exposes the differences.
    L=${LOOPS[${#LOOPS[@]}-1]}
    printf "| strategy | shared n=1 | shared n=2 | shared n=4 | shared n=8 | par n=1 | par n=2 | par n=4 | par n=8 |\n"
    printf "|---|---|---|---|---|---|---|---|---|\n"
    for s in "${STRATEGIES[@]}"; do
        printf "| %s |" "$s"
        for mode in shared parallel; do
            for n in "${THREADS[@]}"; do
                mean=$(awk -F, -v s="$s" -v n="$n" -v l="$L" -v m="$mode" '
                    $1==s && $2==n && $3==l && $4==m && $6!="NA" {sum+=$6; c++}
                    END { if (c>0) printf "%.6f", sum/c; else printf "NA" }' "$CSV")
                printf " %s |" "$mean"
            done
        done
        printf "\n"
    done
    echo
    echo "> \`nolock\` shared-mode numbers are for an **incorrect** result"
    echo "> (it loses updates under contention); they show speed without"
    echo "> correctness, not a usable option. See docs/benchmark-analysis.md."
} > "$SUMMARY"

echo "==> Summary written to $SUMMARY"
